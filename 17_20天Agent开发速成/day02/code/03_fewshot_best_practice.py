# ⭐ 面试官追问：Few-shot 示例怎么选？
"""
3 分钟回答模板：

Few-shot 示例选择有 5 个核心原则：

1. 类别覆盖：每个类别至少 1 个示例（正面/负面/中性都要有）
   → 否则模型会偏向出现过的类别

2. 数量适中：3-5 个最佳
   → 0 个：欠拟合
   → 1-2 个：不够稳定
   → 3-5 个：最佳
   → > 10 个：过拟合，浪费 Token

3. 分布一致：示例的「话题、风格、长度」要和真实输入一致
   → 用电商评论的示例去判断电商评论
   → 不要用电影评论的示例去判断电商评论

4. 顺序优化：和当前问题最相似的示例放在最后
   → 利用近因效应（recency bias）
   → 模型对最近看到的内容印象更深

5. 格式统一：输出格式必须一致
   → "正面" / "negative" / "中性" 混着用 = 模型懵了
   → 要么全中文，要么全英文，要么全标签

特殊情况：
- 任务很简单 → Zero-shot 就够了
- 任务很复杂 → Few-shot + CoT 组合
- 类别失衡（如 95% 都是正面） → 主动加更多少数类示例
"""


